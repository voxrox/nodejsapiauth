const mongoose=require("mongoose")

var UserSchema=mongoose.Schema({
    name:{
        type:String,
        required:true,
    },
    email:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    },
    date:{
        type:Date,
        default:Date.now
    }
})

var userm=mongoose.model("User",UserSchema)

module.exports=userm